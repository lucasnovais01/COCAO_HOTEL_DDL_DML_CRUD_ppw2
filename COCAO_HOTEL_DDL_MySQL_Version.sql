-- T20: COCAO_RESERVA - Recálculo de VALOR_TOTAL após Alterações Críticas
-- Reescrito para BEFORE UPDATE para não causar loop infinito em si mesma
-- ======================================================================
CREATE TRIGGER COCAO_HOTEL_20_TRG_RESERVA_VALOR_RECALCULADO_UPD
BEFORE UPDATE ON COCAO_RESERVA
FOR EACH ROW
BEGIN
    DECLARE v_valor_diaria DECIMAL(10,2);
    DECLARE v_soma_servicos DECIMAL(10,2);

    IF NEW.CODIGO_STATUS IN (1, 2) THEN
        SELECT t.VALOR_DIARIA INTO v_valor_diaria
        FROM COCAO_QUARTO q JOIN COCAO_TIPO_QUARTO t ON q.CODIGO_TIPO_QUARTO = t.CODIGO_TIPO_QUARTO
        WHERE q.ID_QUARTO = NEW.ID_QUARTO;

        SELECT COALESCE(SUM(s.PRECO * hs.QUANTIDADE), 0) INTO v_soma_servicos
        FROM COCAO_HOSPEDE_SERVICO hs JOIN COCAO_SERVICO s ON hs.CODIGO_SERVICO = s.CODIGO_SERVICO
        WHERE hs.ID_RESERVA = NEW.ID_RESERVA
          AND DATE(hs.DATA_SOLICITACAO) >= DATE(NEW.DATA_CHECK_IN)
          AND DATE(hs.DATA_SOLICITACAO) < DATE(NEW.DATA_CHECK_OUT);

        SET NEW.VALOR_TOTAL = (v_valor_diaria * NEW.NUMERO_DIARIAS) + v_soma_servicos;
    END IF;
END //

