DROP VIEW IF EXISTS COCAO_HOTEL_VIEW_01_STATUS_QUARTO;

CREATE VIEW COCAO_HOTEL_VIEW_01_STATUS_QUARTO AS
SELECT 
    q.ID_QUARTO,
    CASE 
        WHEN q.STATUS_QUARTO = 'MANUTENCAO' THEN 'MANUTENCAO'
        WHEN EXISTS (
            SELECT 1
            FROM COCAO_RESERVA r
            JOIN COCAO_STATUS_RESERVA sr ON r.CODIGO_STATUS = sr.CODIGO_STATUS
            WHERE r.ID_QUARTO = q.ID_QUARTO
              AND sr.DESCRICAO IN ('ABERTA', 'ATIVA')
              AND DATE(r.DATA_CHECK_OUT) > CURDATE()
              AND DATE(r.DATA_CHECK_IN) <= CURDATE()
        ) THEN 'OCUPADO'
        ELSE 'LIVRE'
    END AS STATUS_QUARTO,
    CURDATE() AS DATA_INICIO,
    CURDATE() AS DATA_FIM
FROM COCAO_QUARTO q;

-- ======================================================================
-- COCAO_HOTEL_SP_STATUS_QUARTO - Transformada em PROCEDURE para MySQL
-- ======================================================================
DROP PROCEDURE IF EXISTS COCAO_HOTEL_SP_STATUS_QUARTO;

DELIMITER //

CREATE PROCEDURE COCAO_HOTEL_SP_STATUS_QUARTO (
    IN p_data_inicio DATE,
    IN p_data_fim    DATE
)
BEGIN
    IF p_data_inicio IS NOT NULL AND p_data_fim IS NOT NULL AND p_data_inicio > p_data_fim THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20099] COCAO_HOTEL_SF_STATUS_QUARTO: Período inválido.';
    END IF;

    SELECT 
        q.ID_QUARTO,
        CASE 
            WHEN q.STATUS_QUARTO = 'MANUTENCAO' THEN 'MANUTENCAO'
            WHEN EXISTS (
                SELECT 1
                FROM COCAO_RESERVA r
                JOIN COCAO_STATUS_RESERVA sr ON r.CODIGO_STATUS = sr.CODIGO_STATUS
                WHERE r.ID_QUARTO = q.ID_QUARTO
                  AND sr.DESCRICAO IN ('ABERTA', 'ATIVA')
                  AND DATE(r.DATA_CHECK_OUT) > IFNULL(DATE(p_data_inicio), CURDATE())
                  AND DATE(r.DATA_CHECK_IN) <= IFNULL(DATE(p_data_fim), CURDATE())
            ) THEN 'OCUPADO'
            ELSE 'LIVRE'
        END AS STATUS_QUARTO,
        IFNULL(DATE(p_data_inicio), CURDATE()) AS DATA_INICIO,
        IFNULL(DATE(p_data_fim), CURDATE()) AS DATA_FIM
    FROM COCAO_QUARTO q;
END //

