-- 1. COCAO_HOSPEDE (Superclasse - Generalização)
-- ======================================================================
CREATE TABLE COCAO_HOSPEDE (
    ID_USUARIO INT AUTO_INCREMENT,
    NOME_HOSPEDE VARCHAR(100) NOT NULL,
    CPF CHAR(11) NOT NULL,
    RG VARCHAR(20),
    SEXO CHAR(1) NOT NULL,
    DATA_NASCIMENTO DATE NOT NULL,
    EMAIL VARCHAR(100),
    TELEFONE VARCHAR(20),
    TIPO INT DEFAULT 0 NOT NULL,
    ATIVO INT DEFAULT 1,

    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
    -- PRIMARY KEY
    CONSTRAINT PK_COCAO_HOSPEDE PRIMARY KEY (ID_USUARIO),
    -- UNIQUE
    CONSTRAINT UK_COCAO_HOSPEDE_CPF     UNIQUE (CPF),
    -- CHECK
    CONSTRAINT CHK_COCAO_HOSPEDE_SEXO   CHECK (SEXO IN ('M','F','O')),
    CONSTRAINT CHK_COCAO_HOSPEDE_TIPO   CHECK (TIPO IN (0, 1)),
    CONSTRAINT CHK_COCAO_HOSPEDE_ATIVO  CHECK (ATIVO IN (1, 0)),
    -- CHECK COM REGEX PARA MYSQL
    CONSTRAINT CHK_COCAO_HOSPEDE_CPF    CHECK (CPF REGEXP '^[0-9]{11}$'),
    CONSTRAINT CHK_COCAO_HOSPEDE_EMAIL  CHECK (EMAIL IS NULL OR EMAIL REGEXP '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$'),
    CONSTRAINT CHK_COCAO_HOSPEDE_TEL    CHECK (TELEFONE IS NULL OR TELEFONE REGEXP '^[0-9\\s\\-\\(\\)]+$'),
    CONSTRAINT CHK_COCAO_HOSPEDE_RG     CHECK (RG IS NULL OR RG REGEXP '^[0-9A-Za-z\\-]{7,9}$')
);

