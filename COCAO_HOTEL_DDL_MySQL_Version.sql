-- ======================================================================
-- T11: COCAO_HOSPEDE - Impede Exclusão com Reservas Ativas
-- ======================================================================
CREATE TRIGGER COCAO_HOTEL_11_TRG_HOSPEDE_DELETE
BEFORE DELETE ON COCAO_HOSPEDE
FOR EACH ROW
BEGIN
    DECLARE v_count_func INT;
    DECLARE v_count_res  INT;

    IF OLD.TIPO = 1 THEN
        SELECT COUNT(*) INTO v_count_func FROM COCAO_FUNCIONARIO WHERE ID_USUARIO = OLD.ID_USUARIO;
        IF v_count_func > 0 THEN
            SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20020] COCAO_HOSPEDE/T11: Exclusão de funcionário bloqueada. Exclua primeiro o registro em COCAO_FUNCIONARIO.';
        END IF;
    END IF;

    SELECT COUNT(*) INTO v_count_res FROM COCAO_RESERVA
    WHERE ID_USUARIO = OLD.ID_USUARIO AND CODIGO_STATUS IN (1, 2, 3) AND CURDATE() < DATE(DATA_CHECK_OUT);

    IF v_count_res > 0 THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20021] COCAO_HOSPEDE/T11: Exclusão de hóspede bloqueada. Hóspede possui reservas ativas.';
    END IF;
END //

-- ======================================================================
-- T12: COCAO_QUARTO - Validação de Status em MANUTENCAO
-- ======================================================================
CREATE TRIGGER COCAO_HOTEL_12_TRG_STATUS_QUARTO
BEFORE UPDATE ON COCAO_QUARTO
FOR EACH ROW
BEGIN
    DECLARE v_count INT;
    
    IF NEW.STATUS_QUARTO = 'MANUTENCAO' THEN
        SELECT COUNT(*) INTO v_count FROM COCAO_RESERVA
        WHERE ID_QUARTO = NEW.ID_QUARTO AND CODIGO_STATUS IN (1, 2) AND DATE(DATA_CHECK_OUT) > CURDATE();
        
        IF v_count > 0 THEN
            SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20030] COCAO_QUARTO/T12: Não é possível alterar quarto para MANUTENCAO. Quarto possui reservas ativas/abertas.';
        END IF;
    END IF;
END //

-- ======================================================================
-- T13: COCAO_RESERVA - Atualização de STATUS_QUARTO
-- ======================================================================
CREATE TRIGGER COCAO_HOTEL_13_TRG_RESERVA_STATUS_QUARTO_INS
AFTER INSERT ON COCAO_RESERVA
FOR EACH ROW
BEGIN
    DECLARE v_status_quarto VARCHAR(20);
    DECLARE v_count INT;

    SELECT STATUS_QUARTO INTO v_status_quarto FROM COCAO_QUARTO WHERE ID_QUARTO = NEW.ID_QUARTO;

    IF v_status_quarto != 'MANUTENCAO' THEN
        IF NEW.CODIGO_STATUS = 2 AND DATE(NEW.DATA_CHECK_OUT) > CURDATE() THEN
            SET v_count = 1;
        ELSE
            SET v_count = 0;
        END IF;

        UPDATE COCAO_QUARTO SET STATUS_QUARTO = CASE WHEN v_count > 0 THEN 'OCUPADO' ELSE 'LIVRE' END WHERE ID_QUARTO = NEW.ID_QUARTO;
    END IF;
END //

CREATE TRIGGER COCAO_HOTEL_13_TRG_RESERVA_STATUS_QUARTO_UPD
AFTER UPDATE ON COCAO_RESERVA
FOR EACH ROW
BEGIN
    DECLARE v_status_quarto VARCHAR(20);
    DECLARE v_count INT;

    SELECT STATUS_QUARTO INTO v_status_quarto FROM COCAO_QUARTO WHERE ID_QUARTO = NEW.ID_QUARTO;

    IF v_status_quarto != 'MANUTENCAO' THEN
        IF NEW.CODIGO_STATUS = 2 AND DATE(NEW.DATA_CHECK_OUT) > CURDATE() THEN
            SET v_count = 1;
        ELSE
            SET v_count = 0;
        END IF;

