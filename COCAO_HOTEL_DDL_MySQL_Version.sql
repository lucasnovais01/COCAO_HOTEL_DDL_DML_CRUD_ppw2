-- ======================================================================
-- T16: COCAO_SERVICO - Atualiza VALOR_TOTAL de Reservas
-- ======================================================================
CREATE TRIGGER COCAO_HOTEL_16_TRG_ATUALIZA_SERVICO
AFTER UPDATE ON COCAO_SERVICO
FOR EACH ROW
BEGIN
    UPDATE COCAO_RESERVA r
    SET VALOR_TOTAL = (
        SELECT t.VALOR_DIARIA * r.NUMERO_DIARIAS
        FROM COCAO_QUARTO q JOIN COCAO_TIPO_QUARTO t ON q.CODIGO_TIPO_QUARTO = t.CODIGO_TIPO_QUARTO
        WHERE q.ID_QUARTO = r.ID_QUARTO
    ) + COALESCE((
        SELECT SUM(s.PRECO * hs.QUANTIDADE)
        FROM COCAO_HOSPEDE_SERVICO hs JOIN COCAO_SERVICO s ON hs.CODIGO_SERVICO = s.CODIGO_SERVICO
        WHERE hs.ID_RESERVA = r.ID_RESERVA
          AND DATE(hs.DATA_SOLICITACAO) >= DATE(r.DATA_CHECK_IN) AND DATE(hs.DATA_SOLICITACAO) < DATE(r.DATA_CHECK_OUT)
    ), 0)
    WHERE r.CODIGO_STATUS IN (1, 2)
      AND EXISTS (
          SELECT 1 FROM COCAO_HOSPEDE_SERVICO hs
          WHERE hs.ID_RESERVA = r.ID_RESERVA AND hs.CODIGO_SERVICO = NEW.CODIGO_SERVICO
            AND DATE(hs.DATA_SOLICITACAO) >= DATE(r.DATA_CHECK_IN) AND DATE(hs.DATA_SOLICITACAO) < DATE(r.DATA_CHECK_OUT)
      );
END //

