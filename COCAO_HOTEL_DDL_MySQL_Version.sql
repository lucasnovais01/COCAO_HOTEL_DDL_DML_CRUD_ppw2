-- ======================================================================
-- T14: COCAO_RESERVA - Controle de Transição de Status
-- ======================================================================
CREATE TRIGGER COCAO_HOTEL_14_TRG_RESERVA_STATUS_TRANSICAO
BEFORE UPDATE ON COCAO_RESERVA
FOR EACH ROW
BEGIN
    IF OLD.CODIGO_STATUS != NEW.CODIGO_STATUS THEN
        CASE OLD.CODIGO_STATUS
            WHEN 1 THEN
                IF NEW.CODIGO_STATUS NOT IN (2, 3, 5) THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20016] Transição de ABERTA inválida.'; END IF;
            WHEN 2 THEN
                IF NEW.CODIGO_STATUS NOT IN (3, 4) THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20017] Transição de ATIVA inválida.'; END IF;
                IF NEW.CODIGO_STATUS = 4 AND DATE(OLD.DATA_CHECK_OUT) > CURDATE() THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20024] Finalização prematura.'; END IF;
            WHEN 3 THEN
                IF NEW.CODIGO_STATUS NOT IN (4, 5) THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20018] Transição de A_PAGAR inválida.'; END IF;
                IF NEW.CODIGO_STATUS = 4 AND DATE(OLD.DATA_CHECK_OUT) > CURDATE() THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20024] Finalização prematura.'; END IF;
            WHEN 4 THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20023] Reserva FINALIZADA.';
            WHEN 5 THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20023] Reserva CANCELADA.';
            ELSE SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = '[-20012] Status inválido.';
        END CASE;
    END IF;
END //

